package programs;

public class shift_operator {
	
	public static void main(string[] args) {
		
		//Right shift-->operand/2^num
		
		System.out.println(10>>5);
		System.out.println(10>>2);
		System.out.println(20>>4);
		
		//left shift-->operand*2^num
		System.out.println(10>>5);
		System.out.println(10>>2);
		System.out.println(20>>4);
	}

}
